$factory->define(User::class, function (Faker $faker) {
    return [
        'name' => $faker->name,
        'surname' => $faker->sentence,
        'email' => $faker->unique()->safeEmail,
        'event_id' => Event::pluck('id')[$faker->numberBetween(1,Event::count()-1)]
    ];
});
